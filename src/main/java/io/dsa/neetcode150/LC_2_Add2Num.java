package io.dsa.neetcode150;

import java.io.StringBufferInputStream;

public class LC_2_Add2Num {
    private static ListNode Solution(ListNode l1, ListNode l2) {
        /*
        Input: l1 = [2,4,3], l2 = [5,6,4]
        Output: [7,0,8]
        Explanation: 342 + 465 = 807.
         */
        /*
         we can do onething that we can traverse and add if the carry comes store it in a variable
         every operation
         */
        ListNode dummy = new ListNode();
        ListNode res = dummy;
        int total = 0, carry = 0;

        while (l1 != null || l2 != null || carry != 0) {
            total = carry;

            if (l1 != null) {
                total += l1.val;
                l1 = l1.next;
            }
            if (l2 != null) {
                total += l2.val;
                l2 = l2.next;
            }

            int num = total % 10;
            carry = total / 10;
            dummy.next = new ListNode(num);
            dummy = dummy.next;
        }

        return res.next;

    }

}

